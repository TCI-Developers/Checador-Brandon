    private void jDatePropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDatePropertyChange
        String fecha = Fecha.FormateToText(jDate);
        System.out.println(fecha);
        if(fecha != null && !fecha.equals(fechaAux)){
            ExecutorService executorVolumen = Executors.newSingleThreadExecutor();
            executorVolumen.submit(()->{ 
                jpbEnvio.setVisible(!enableBtn);
                lblLeyenda.setVisible(enableBtn);
                btnEnviar.setEnabled(enableBtn);
                ctrlHttp.cargarRegitros(fecha);
            });
            fechaAux = fecha;
        }
       
    }//GEN-LAST:event_jDatePropertyChange

